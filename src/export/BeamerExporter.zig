test "BeamerExporter basic structure" {
    const testing = std.testing;
    const allocator = testing.allocator;

    var presentation = Presentation{
        .allocator = allocator,
        .metadata = .{
            .title = try allocator.dupe(u8, "Test Presentation"),
            .author = try allocator.dupe(u8, "Test Author"),
            .date = try allocator.dupe(u8, "2026-03-03"),
            .theme = null,
        },
        .slides = try allocator.alloc(Slide, 1),
    };
    defer {
        presentation.metadata.deinit(allocator);
        for (presentation.slides) |slide| {
            slide.deinit(allocator);
        }
        allocator.free(presentation.slides);
    }

    var elements: std.ArrayList(Element) = .empty;
    defer elements.deinit(allocator);

    const heading_content = try allocator.alloc(@import("../core/Element.zig").Inline, 1);
    heading_content[0] = .{ .text = try allocator.dupe(u8, "Hello World") };

    try elements.append(allocator, .{ .heading = .{
        .content = heading_content,
        .level = 1,
    } });

    presentation.slides[0] = Slide{
        .elements = try elements.toOwnedSlice(allocator),
        .speaker_notes = null,
    };

    var exporter = BeamerExporter.init(allocator, null, null);
    const latex = try exporter.exportToLatex(presentation);
    defer allocator.free(latex);

    // Check basic structure
    try testing.expect(std.mem.containsAtLeast(u8, latex, 1, "\\documentclass"));
    try testing.expect(std.mem.containsAtLeast(u8, latex, 1, "\\begin{document}"));
    try testing.expect(std.mem.containsAtLeast(u8, latex, 1, "\\end{document}"));
    try testing.expect(std.mem.containsAtLeast(u8, latex, 1, "\\begin{frame}"));
    try testing.expect(std.mem.containsAtLeast(u8, latex, 1, "\\end{frame}"));
    try testing.expect(std.mem.containsAtLeast(u8, latex, 1, "Test Presentation"));
    try testing.expect(std.mem.containsAtLeast(u8, latex, 1, "Test Author"));
}

